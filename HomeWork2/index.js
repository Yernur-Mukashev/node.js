function plus(x, y) {
	return x + y;
}

function minus(x, y) {
	return x - y;
}

function mult(x, y) {
	return x * y;
}

function division(x, y) {
	return x / y;
}

function takingRemainder(x, y) {
	return x % y;
}

function exponentiation(x, y) {
	return x ** y;
}

module.exports = {
	plus,
	minus,
	mult,
	division,
	takingRemainder,
	exponentiation,
};
